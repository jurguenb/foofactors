#' Bind two factos
#'
#'Create a new factor from two existing factors, where the new factor's levels
#'Create a new factor from two existing factors, where the new factor's levels
#'
#' @param a factor
#' @param b factor
#'
#' @return factor
#' @export
#'
#' @examples
#' fbind(iris$Species[c(1, 51, 101)], PlantGrowth$group[c(1, 11, 21)])
fbind <- function(a, b) {
  factor(c(as.character(a), as.character(b)))
}
